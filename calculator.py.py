num1 = int(input("enter your first number:"))
oper = input("choose your operator - +,-,/,*:")
num2 = int(input("enter your scond number:"))
if oper == "+":
    print(num1 + num2)
elif oper == "-":
    print(num1 - num2)
elif oper == "/":
    print(num1/num2)
elif oper == "*":
    print(num1*num2)